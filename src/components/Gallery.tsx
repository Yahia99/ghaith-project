import * as React from "react";
import { useState } from "react";
import ImageList from "@mui/material/ImageList";
import ImageListItem from "@mui/material/ImageListItem";
import ImageListItemBar from "@mui/material/ImageListItemBar";
import ListSubheader from "@mui/material/ListSubheader";
import IconButton from "@mui/material/IconButton";
import InfoIcon from "@mui/icons-material/Info";
import Container from "@mui/material/Container";
import { useMediaQuery, useTheme } from "@mui/material";

function Gallery() {
  const theme = useTheme();
  const isSmallScreen = useMediaQuery(theme.breakpoints.down("sm"));
  const [showItemBar, setShowItemBar] = useState<boolean>(false);
  const [counter, setCounter] = useState<{ counter: number }>({ counter: 0 });

  return (
    <Container maxWidth="lg">
      <ImageList gap={20}>
        <ImageListItem key="Subheader" cols={2}>
          <ListSubheader component="div">December</ListSubheader>
        </ImageListItem>
        {itemData.map((item, index) => (
          <ImageListItem
            key={item.img}
            onMouseEnter={() => {
              setShowItemBar(true);
              setCounter({ ...counter, counter: index });
            }}
            onMouseLeave={() => setShowItemBar(false)}
          >
            <img
              srcSet={`${item.img}?w=248&fit=crop&auto=format&dpr=2 2x`}
              src={`${item.img}?w=248&fit=crop&auto=format`}
              alt={item.title}
              loading="lazy"
            />
            {showItemBar && index === counter.counter ? (
              <ImageListItemBar
                title={item.title}
                subtitle={item.author}
                actionIcon={
                  <IconButton
                    sx={{ color: "rgba(255, 255, 255, 0.54)" }}
                    aria-label={`info about ${item.title}`}
                  >
                    <InfoIcon />
                  </IconButton>
                }
              />
            ) : (
              ""
            )}
          </ImageListItem>
        ))}
      </ImageList>
    </Container>
  );
}

